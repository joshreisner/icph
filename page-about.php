			<p>Conditions that perpetuate homelessness and poverty today have deep roots in America’s past. In <em>The Poor Among Us: A History of Family Poverty and Homelessness in New York City</em>, Ralph da Costa Nunez and Ethan G. Sribnick explore the world of New York’s poor children and families, from the era of European settlement to the present day: their physical and social environments, the causes of their poverty, and the institutions and social movements that evolved to improve and regulate their lives. This comprehensive history examines the successes and failures of past efforts to reduce poverty and homelessness, providing the historical context that is often lacking in contemporary policy debates.

