			<p>The Institute for Children, Poverty, and Homelessness (ICPH) is an independent nonprofit research and policy-analysis organization whose work illuminates the complex issue of family homelessness and informs and enhances public policy related to homeless families, especially children.</p>

<p>Child and family poverty in New York City are not new. Destitute families and homeless children have existed throughout American history, especially in urban areas. This long history of poverty, and of public and private relief efforts, continues to offer lessons for those shaping public policy today. For this reason, ICPH has made a commitment—unique among research organizations of its kind—to in-depth historical research on family poverty and homelessness in New York City.
</p>

